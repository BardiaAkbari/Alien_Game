class Settings:
    """A Class for storing all settings of game"""

    def __init__(self):

        # Game screen Settings
        self.width = 800
        self.height = 600
        self.back_color = (255, 255, 255)
        # Ship Settings
        self.ship_speed = 2.0
        # Bullet Settings
        self.bullet_width = 3
        self.bullet_height = 15
        self.bullet_color = (50, 50, 50)
        self.bullet_speed = 1.0
        self.number_of_allowed_bullet = 5





